<h1 class="mt-8 font-accent text-3xl">About Harlequin</h1>
<h2 class="mt-8 font-accent text-2xl">Who</h2>
<p class="mt-2">
  Harlequin was created by <a
    href="https://tedconbeer.com"
    target="_blank"
    class="underline hover:decoration-green hover:decoration-4 hover:underline-offset-4"
    >Ted Conbeer</a
  > in 2023.
</p>

<h2 class="mt-8 font-accent text-2xl">What</h2>
<p class="mt-2">
  Harlequin is a SQL IDE for <a
    href="https://duckdb.org/"
    target="_blank"
    class="underline hover:decoration-green hover:decoration-4 hover:underline-offset-4"
    >DuckDB</a
  >,
  <a
    href="https://sqlite.org/"
    target="_blank"
    class="underline hover:decoration-green hover:decoration-4 hover:underline-offset-4"
    >SQLite</a
  >, and other databases that runs in your Terminal.
</p>
<p class="mt-2">
  A harlequin is also a <a
    href="https://en.wikipedia.org/wiki/Harlequin_duck"
    target="_blank"
    class="underline hover:decoration-green hover:decoration-4 hover:underline-offset-4"
    >pretty duck.</a
  >
</p>

<h2 class="mt-8 font-accent text-2xl">How</h2>
<p class="mt-2">
  Harlequin is written in Python, using the <a
    href="https://textual.textualize.io/#what-is-textual"
    target="_blank"
    class="underline hover:decoration-green hover:decoration-4 hover:underline-offset-4"
    >Textual</a
  > framework.
</p>

